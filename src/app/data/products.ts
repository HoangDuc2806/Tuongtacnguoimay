export const getCategoryProducts = (category?: string) => {
  if (!category || category === "all") return products;
  
  const categoryMap: Record<string, string[]> = {
    new: products.filter(p => p.badge === "New").map(p => p.id),
    sale: products.filter(p => p.badge === "Sale").map(p => p.id),
    organic: products.filter(p => p.badge === "Organic").map(p => p.id),
    recycled: products.filter(p => p.badge === "Recycled").map(p => p.id),
    "ao-thun": products.filter(p => p.category === "Áo thun").map(p => p.id),
    "ao-hoodie": products.filter(p => p.category === "Áo hoodie").map(p => p.id),
    "ao-khoac": products.filter(p => p.category === "Áo khoác").map(p => p.id),
    "quan": products.filter(p => p.category === "Quần").map(p => p.id),
    "giay": products.filter(p => p.category === "Giày").map(p => p.id),
    "phu-kien": products.filter(p => p.category === "Phụ kiện").map(p => p.id),
  };

  const ids = categoryMap[category.toLowerCase()];
  if (!ids) return products;
  
  return products.filter(p => ids.includes(p.id));
};
